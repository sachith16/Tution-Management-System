    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        final int index = jTable3.getSelectedRow();
        String conumber;
        if (index >= 0) {
            conumber = (String) jTable3.getValueAt(index, 0);

            try {
                boolean isAdded = freeHalfController.deleteName(ClassID, conumber);
                if (isAdded) {
                    JOptionPane.showMessageDialog(freeHalf.this, "Record deleted successfully");
                    searchm2();
                } else {
                    JOptionPane.showMessageDialog(freeHalf.this, "Record deleting failed");

                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(freeHalf.this, ex.getMessage());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(freeHalf.this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

