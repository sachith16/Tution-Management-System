    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_PAGE_DOWN) {
            int n = 10 - jTextField1.getText().length();
            for (int i = 0; i < n; i++) {
                jTextField1.setText("0" + jTextField1.getText());
            }

        }

        if (evt.getKeyCode() == KeyEvent.VK_ENTER && !jTextField1.getText().isEmpty()) {
            try {
                boolean isAdded = freeHalfController.addNameFree(ClassID, jTextField1.getText());

                if (isAdded) {
                    jTextField1.setText("");
                    searchm2();
                } else {
                    JOptionPane.showMessageDialog(freeHalf.this, "Adding failed");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(freeHalf.this, ex.getMessage());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(freeHalf.this, ex.getMessage());
            }
        }

        if (!jTextField1.getText().isEmpty()) {
            jButton1.setEnabled(true);
        } else {
            jButton1.setEnabled(false);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

