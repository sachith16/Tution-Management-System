    public void searchm2() {
        mod.setRowCount(0);
        mod2.setRowCount(0);
        try {
            studentModel[] clsM = controller.freeHalfController.viewStudentFree(ClassID);
            studentModel[] clsM2 = controller.freeHalfController.viewStudentHalf(ClassID);

            for (int i = 0; i < clsM.length; i++) {
                String rowmod[] = {clsM[i].getStID(), clsM[i].getName(), clsM[i].getAddress(), clsM[i].getSchool(), clsM[i].getTelM() + "/" + clsM[i].getTelH()};
                mod.addRow(rowmod);
            }

            for (int i = 0; i < clsM2.length; i++) {
                String rowmod[] = {clsM2[i].getStID(), clsM2[i].getName(), clsM2[i].getAddress(), clsM2[i].getSchool(), clsM2[i].getTelM() + "/" + clsM2[i].getTelH()};
                mod2.addRow(rowmod);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(freeHalf.this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(freeHalf.this, ex.getMessage());
        }

    }

